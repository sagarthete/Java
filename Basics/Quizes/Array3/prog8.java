
     class ArrayDemo{
     
           public static void main(String args[]){
	   
	       char arr[] = new char[3];

	       arr[0] = 'C';
	       arr[1] = '2';
	       arr[2] = 'W';

	      for(int i=0;i<arr.length;i++){
	      
	          System.out.println(arr[i]+" ");
	      } 
	      System.out.println();
	   }
      
     }
